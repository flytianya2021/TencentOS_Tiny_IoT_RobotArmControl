/*占空比 = （Pulse/Period）*100%；频率: f = 48M/Prescaler/Period；影响PWM占空比跟频率的参数这主要是 Pulse、Prescaler、Period 这3个；
频率跟占空比都与Period（计数周期）有关，只要我们把计数周期定下来，修改Prescaler、跟Pulse这两个值，就可以直接修改PWM的频率跟占空比了。
具体要多少的占空比跟频率，就自己计算一下就可以了。举个例子说明一下：这里Period设为100，如果我要设定一个频率为1500Hz、占空比5%的PWM波，
只需要传入参数Pulse=5（占空比 = Pulse / Period * 100% =（5/100） * 100%=5%）、Prescaler=320（f = 系统频率 / 分频 / 计数周期 = 48M/320/100 = 1500Hz）即可*/
